list_entry_t pra_list_head2;
/*
 * (2) _lru_init_mm: init pra_list_head and let  mm->sm_priv point to the addr of pra_list_head.
 *              Now, From the memory control struct mm_struct, we can access FIFO PRA
 * （2）_lru_init_mm：初始化pra_list_head并且让mm->sm_priv指向pra_list_head的地址。
 * 现在，从内存控制mm_struct，我们可以进入LRU
 */
static int
_lru_init_mm(struct mm_struct *mm)
{     
     list_init(&pra_list_head2);
     mm->sm_priv = &pra_list_head2;
     //cprintf(" mm->sm_priv %x in fifo_init_mm\n",mm->sm_priv);
     return 0;
}
/*
 * (3)_lru_map_swappable: According FIFO PRA, we should link the most recent arrival page at the back of pra_list_head qeueue
 *（3）LRU策略改动的地方：
 * 如果该页面曾经被访问到过，重新放到队尾
 */
static int
_lru_map_swappable(struct mm_struct *mm, uintptr_t addr, struct Page *page, int swap_in)
{
    list_entry_t *head=(list_entry_t*) mm->sm_priv;
    list_entry_t *entry=&(page->pra_page_link);
 
    assert(entry != NULL && head != NULL);

    // 检查页面是否已经在列表中
    list_entry_t* current = list_next(head);
    while (current != head) {
        if (current == entry) {
            // 页面已经在列表中，删除
            list_del(entry);
            break;
        }
        current = list_next(current);
    }
    list_add(head, entry);
    return 0;
}
/*
 *  (4)_fifo_swap_out_victim: According FIFO PRA, we should unlink the  earliest arrival page in front of pra_list_head qeueue,
 *                            then set the addr of addr of this page to ptr_page.
 * （4）取消队头的链接，将addr设置为ptr_page
 */
static int
_lru_swap_out_victim(struct mm_struct *mm, struct Page ** ptr_page, int in_tick)
{
     list_entry_t *head=(list_entry_t*) mm->sm_priv;
         assert(head != NULL);
     assert(in_tick==0);
     /* Select the victim */
     //(1)  unlink the  earliest arrival page in front of pra_list_head qeueue
     //(2)  set the addr of addr of this page to ptr_page
    list_entry_t* entry = list_prev(head);
    if (entry != head) {
        list_del(entry);
        *ptr_page = le2page(entry, pra_page_link);
    } else {
        *ptr_page = NULL;
    }
    return 0;
}

